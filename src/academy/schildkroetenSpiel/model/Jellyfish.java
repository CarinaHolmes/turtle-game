package academy.schildkroetenSpiel.model;

public class Jellyfish {

    private double posX;
    private double posY;

    Jellyfish (double posX, double posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public double getPosX() {
        return posX;
    }

    void setPosX(double posX) {
        this.posX = posX;
    }

    public double getPosY() {
        return posY;
    }


}
